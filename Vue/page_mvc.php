<h1>Model–view–controller (MVC)</h1>
<h2>Definition</h2>
<p>Model–view–controller is a software design pattern commonly used for developing user interfaces that divides the related program logic into three interconnected elements. These elements are:</p>

<ul>
    <li>the model, the internal representations of information</li>
    <li>the view, the interface that presents information to and accepts it from the user</li>
    <li>the controller, the software linking the two.</li>
</ul>
<p>Traditionally used for desktop graphical user interfaces (GUIs), this pattern became popular for designing web applications.[4] Popular programming languages have MVC frameworks that facilitate the implementation of the pattern.</p>
