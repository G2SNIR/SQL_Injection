<h1>Model–view–controller (MVC)</h1>
<h2>Definition</h2>
<p>Model–view–controller is a software design pattern commonly used for developing user interfaces that divides the related program logic into three interconnected elements. These elements are:</p>

<ul>
    <li>the model, the internal representations of information</li>
    <li>the view, the interface that presents information to and accepts it from the user</li>
    <li>the controller, the software linking the two.</li>
</ul>
<p>Traditionally used for desktop graphical user interfaces (GUIs), this pattern became popular for designing web applications. Popular programming languages have MVC frameworks that facilitate the implementation of the pattern.</p>
<h2>Model</h2>
<p>The model is the central component of the pattern. It is the application's dynamic data structure, independent of the user interface. The model is essential for keeping the data organized and consistent.</p>

<h2>View</h2>
<p>The view is any representation of information such as a chart, diagram or table. Multiple views of the same information are possible, such as a bar chart for management and a tabular view for accountants.</p>

<h2>Controller</h2>
<p>The controller accepts input and converts it to commands for the model. It then prepares the model data and supplies it to the view.</p>
